public class VisualNode {

    //region Instance Vars
    double x;
    double y;
    //endregion

    /**
     * Default constructor
     * @param x
     * @param y
     */
    public VisualNode(double x, double y){
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x
     * @return x
     */
    public double getX() {
        return x;
    }

    /**
     * Gets y
     * @return y
     */
    public double getY() {
        return y;
    }
}